package tema4;

public class If {
    public static void main(String[] args) {

        int numero = 0;
        if (numero < 0) {
            System.out.println("es negativo");
        } else if (numero > 0) {
            System.out.println("es positivo");
        } else {
            System.out.println("es cero");
        }
    }
}
